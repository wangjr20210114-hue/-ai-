export type TranslationKey = keyof typeof catalog;
export const translationKeys = Object.keys(catalog) as TranslationKey[];

const languageIndex: Record<Language, number> = {
  'zh-CN': 0,
  'zh-TW': 1,
  en: 2,
  'cat-cute': 3,
  'cat-cold': 4,
};

export function getStoredLanguage(): Language {
  try {
    const value = localStorage.getItem(LANGUAGE_KEY) as Language | null;
    if (value && value in languageIndex) return value;
  } catch { /* private mode or server render */ }
  return 'zh-CN';
}

export function translate(
  key: TranslationKey,
  params: TranslationParams = {},
  language: Language = getStoredLanguage(),
): string {
  const template = catalog[key][languageIndex[language]] || catalog[key][0];
  return template.replace(/\{(\w+)\}/g, (match, name: string) => (
    Object.prototype.hasOwnProperty.call(params, name) ? String(params[name]) : match
  ));
}

export function languageInstruction(language: Language): string {
  if (language === 'en') return 'Respond in clear, natural English unless the user explicitly requests another language.';
  if (language === 'zh-TW') return '請使用自然、清晰的繁體中文回答；保留 Markdown 結構與連結。';
  if (language === 'cat-cute') return '请使用简体中文回答，语气像一只亲人的可爱橘猫，适度加入“喵”，但保持信息准确、清晰，不要过度卖萌。';
  if (language === 'cat-cold') return '请使用简体中文回答，语气像一只冷静、克制的橘猫，偶尔使用简短的“喵”，不要撒娇，保持信息准确、直接。';
  return '请使用自然、清晰的简体中文回答；保留 Markdown 结构与链接。';
}

export function languageName(language: Language): string {
  const key: Record<Language, TranslationKey> = {
    'zh-CN': 'languageZhCn',
    'zh-TW': 'languageZhTw',
    en: 'languageEn',
    'cat-cute': 'languageCatCute',
    'cat-cold': 'languageCatCold',
  };
  return translate(key[language], {}, language);
}

interface LanguageContextValue {
  language: Language;
  setLanguage: (language: Language) => void;
  t: (key: TranslationKey, params?: TranslationParams) => string;
  modelInstruction: string;
}

const fallbackLanguage: Language = 'zh-CN';
const LanguageContext = createContext<LanguageContextValue>({
  language: fallbackLanguage,
  setLanguage: () => undefined,
  t: (key, params) => translate(key, params, fallbackLanguage),
  modelInstruction: languageInstruction(fallbackLanguage),
});

export function LanguageProvider({ children }: { children: ReactNode }) {
  const [language, setLanguageState] = useState<Language>(() => getStoredLanguage());
  const setLanguage = (next: Language) => {
    setLanguageState(next);
    try { localStorage.setItem(LANGUAGE_KEY, next); } catch { /* ignore */ }
  };
  const value = useMemo(() => ({
    language,
    setLanguage,
    t: (key: TranslationKey, params?: TranslationParams) => translate(key, params, language),
    modelInstruction: languageInstruction(language),
  }), [language]);
  return <LanguageContext.Provider value={value}>{children}</LanguageContext.Provider>;
}

export function useLanguage(): LanguageContextValue {
  return useContext(LanguageContext);
}
